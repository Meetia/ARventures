    float[] mGravity;
    float[] mGeomagnetic;
    public void onSensorChanged(SensorEvent event) {
        TextView azimuthText = (TextView) findViewById(R.id.azimuthText);
        if (event.sensor.getType() == Sensor.TYPE_ACCELEROMETER)
            mGravity = event.values;
        if (event.sensor.getType() == Sensor.TYPE_MAGNETIC_FIELD)
            mGeomagnetic = event.values;
        if (mGravity != null && mGeomagnetic != null) {

            float R[] = new float[9];
            float I[] = new float[9];
            boolean success = SensorManager.getRotationMatrix(R, I, mGravity, mGeomagnetic);

            if (success) {
                float orientation[] = new float[3];
                SensorManager.getOrientation(R, orientation);
                float degree = orientation[0] * (float) (1.0/0.01745329252); // magnetic north orientation
                String azOut = Float.toString(degree); //* (float) 0.01745329252);
                if(geoField != null)
                    declination = geoField.getDeclination();
                //dest_bearing += declination;
                //degree = (dest_bearing - degree) * -1;
                /*
                    degree = heading - compass from self to magnetic north
                    dest_bearing = bearing - bearing from self to destination in magnetic north

                */
                imageView.setRotation(dest_bearing - degree + declination);
                azimuthText.setText(azOut);
            }
        }
    }
